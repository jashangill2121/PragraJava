package Assignments;

public class AddArray {
    public static void main(String[] args) {
        
        int[] number = {10, 30, 43, 54, 23, 543, 43, 32};
        int sum = 0;
        for (int i = 0; i < number.length; i++) {

            sum = sum + number [i];

        }

        System.out.println(sum);
    }
    
    

}
